package pb.ajneb97.api;

public class Perk {

	private String name;
	private int level;
	public Perk(String name, int level) {
		this.name = name;
		this.level = level;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getNivel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	
	
}
